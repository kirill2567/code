#!/bin/bash

# Проверка, есть ли аргументы
if [ $# -eq 0 ]; then
  echo "Ошибка: необходимо передать хотя бы одно число."
  exit 1
fi

# Инициализация переменных
sum=0
count=0

# Проход по всем аргументам
for arg in "$@"; do
  # Проверка, является ли аргумент числом
  if [[ $arg =~ ^-?[0-9]+(\.[0-9]+)?$ ]]; then
    sum=$(echo "$sum + $arg" | bc)
    count=$((count + 1))
  else
    echo "Предупреждение: '$arg' не является числом и будет проигнорировано."
  fi
done

# Проверка, были ли переданы числа
if [ $count -eq 0 ]; then
  echo "Ошибка: ни один аргумент не является числом."
  exit 1
fi

# Вычисление среднего значения
average=$(echo "scale=2; $sum / $count" | bc)
echo "Среднее значение: $average"
