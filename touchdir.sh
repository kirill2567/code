#!/bin/bash

# Проверка, был ли передан аргумент
if [ -z "$1" ]; then
  echo "Ошибка: Необходимо указать путь до директории."
  exit 1
fi

# Проверка существования директории
if [ -d "$1" ]; then
  echo "Директория $1 уже существует."
else
  # Попытка создания директории
  mkdir -p "$1" 2>/dev/null
  if [ $? -eq 0 ]; then
    echo "Директория $1 успешно создана."
  else
    echo "Ошибка: невозможно создать директорию $1. Проверьте права доступа."
  fi
fi
